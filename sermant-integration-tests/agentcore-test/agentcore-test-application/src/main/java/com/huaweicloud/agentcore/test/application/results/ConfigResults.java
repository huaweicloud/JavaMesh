package com.huaweicloud.agentcore.test.application.results;

/**
 * 核心配置和插件配置加载测试用例结果
 *
 * @author tangle
 * @since 2023-10-09
 */
public enum ConfigResults {
    /**
     * 插件配置加载
     */
    PLUGIN_CONFIG_LOADED_SUCCESS("Test load plugin config."),

    /**
     * 核心配置加载
     */
    CORE_CONFIG_LOADED_SUCCESS("Test load core config.");

    /**
     * 用例描述
     */
    private String description;

    /**
     * 测试结果标识
     */
    private boolean result;

    /**
     * 构造函数
     *
     * @param description 用例描述
     */
    ConfigResults(String description) {
        this.description = description;
        this.result = false;
    }

    public boolean getResult() {
        return result;
    }

    public void setResult(boolean result) {
        this.result = result;
    }

    public String getDescription() {
        return description;
    }
}
